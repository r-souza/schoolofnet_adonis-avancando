class Simple {
  /**
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Function} next
   */
  async handle ({ request }, next) {
    // call next to advance the request
    console.log('Hello from my Simple Middleware')
    console.log(`${request.url()} - ${request.hostname()}`)

    await next()
  }
}

module.exports = Simple
